package fingerspelling_recognition.db_utils;

public class Parameter {
	private int move;
	private double time;

	private int side;
	private int height;

	private int angle;



	public int getMove() {
		return move;
	}

	public void setMove(int move) {
		this.move = move;
	}

	public double getTime() {
		return time;
	}

	public void setTime(double time) {
		this.time = time;
	}

	public int getSide() {
		return side;
	}

	public void setSide(int side) {
		this.side = side;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getAngle() {
		return angle;
	}

	public void setAngle(int angle) {
		this.angle = angle;
	}


}
